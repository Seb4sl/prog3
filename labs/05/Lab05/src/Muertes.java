public class Muertes {
    char genero;
    String edad;
    String fecha;
    String caracteristica;
    String columna;
    String clase_acc;
    String v_victima;
    String v_contra;

    public Muertes(char genero, String edad, String fecha, String caracteristica, String columna, String clase_acc, String v_victima) {
        this.genero = genero;
        this.edad = edad;
        this.fecha = fecha;
        this.caracteristica = caracteristica;
        this.columna = columna;
        this.clase_acc = clase_acc;
        this.v_victima = v_victima;
        this.v_contra = v_contra;
    }
    
    
}
